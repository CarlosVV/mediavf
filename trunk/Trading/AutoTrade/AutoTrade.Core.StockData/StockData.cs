namespace AutoTrade.Core.StockData
{
    public class StockData
    {
        /// <summary>
        /// Gets or sets the symbol for the stock
        /// </summary>
        public string Symbol { get; set; }

        /// <summary>
        /// Gets or sets the company name for the stock
        /// </summary>
        public string CompanyName { get; set; }

        /// <summary>
        /// Gets or sets the sector of the stock
        /// </summary>
        public string Sector { get; set; }

        /// <summary>
        /// Gets or sets the industry of the stock
        /// </summary>
        public string Industry { get; set; }
    }
}